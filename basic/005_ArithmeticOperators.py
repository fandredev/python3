"""
  Adição: + 
  Multiplicação: *
  Subtração: -
  Divisão: /
  Módulo: %
  Divisão inteira: // 
  Precendencia: ()
"""

print('Adição : (+)', 10+10)
print('Multiplicação : (*)', 10*10)
print('Subtração : (-)', 10-10)
print('Divisão : (/)', 10/10)
print('Divisão inteira : (//)', 10//10)
print('Módulo : (%)', 10%10)
print('Precedência : ( () )', (10+10) * 2 )