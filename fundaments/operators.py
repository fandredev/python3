# Operadores aritiméticos


print(10+5) # Soma
print(10.5+5) # Soma com float
print(50*5) # Multiplicação
print(10.5-5) # Subtração
print(10/3) # Dízima periodica
print(10//3) # Resolve dízima periodica
print(10%2) # Módulo
print(3**2) # Exponênciação


